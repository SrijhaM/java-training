public class AddNumberStrings 
{
    public static void main(String[] args) 
    {
        String num1 = "124";
        String num2 = "456";
        int n1 = Integer.parseInt(num1);
        int n2 = Integer.parseInt(num2);
        int sum = n1 + n2;
        System.out.println("Sum: " + sum);
    }
}
 